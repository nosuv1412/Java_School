    public void clickTable(int selectedRow){
        Vector datas=new Vector();
        XLDL xl=new XLDL();
        datas=xl.getRetangle();
        Vector dataSelected=new Vector();
        dataSelected=(Vector) datas.get(selectedRow);
        int id=(int) dataSelected.get(0);
        
        Retangle r=(Retangle) dataSelected.get(1);
        String color=r.getColor();
        boolean filled = r.isFilled();
        double width= r.getWidth();
        double length=r.getLength();
        double area=r.getArena();
        double perimeter=r.getPerimeter();
        
        //Set dlieu vao textbox
        txtID.setText(id+"");
        txtFilled.setText(filled+"");
        cbColor.setSelectedItem(color);
        txtWidth.setText(width+"");
        txtLength.setText(length+"");
        txtArea.setText(area+"");
        txtPerimeter.setText(perimeter+"");
    }
    
    public void displayTable(){
        Vector header = new Vector();
        Vector datas = new Vector();
        
        header.add("ID");
        header.add("Color");
        header.add("Filled");
        header.add("Width");
        header.add("Length");
        header.add("Area");
        header.add("Perimeter");
        
        XLDL xl = new XLDL();
        Vector tmp = xl.getRetangle();
        for(int i=0; i<tmp.size(); i++){
            Vector v = (Vector) tmp.get(i);
            int id = (int)v.get(0);
            Retangle r = (Retangle) v.get(1);
            double width = r.getWidth();
            boolean filled = r.isFilled();
            String color = r.getColor();
            double length = r.getLength();
            double area = r.getArena();
            double perimeter = r.getPerimeter();
            
            Vector data = new Vector();
            data.add(id);
            data.add(color);
            data.add(filled);
            data.add(width);
            data.add(length);
            data.add(area);
            data.add(perimeter);
            
            datas.add(data);  
        }
        tbRectangle.setModel(new DefaultTableModel(datas,header));
    }
    
    
